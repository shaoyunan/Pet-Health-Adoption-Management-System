    private void populatePersonComboBox(Organization organization) {
        cmbPerson.removeAllItems();

        for (Person person : organization.getPersonDirectory().getPersonList()) {
            cmbPerson.addItem(person);
        }
    }

    private void populateRoleComboBox(Organization organization) {
        cmbRole.removeAllItems();
        for (Role role : organization.getSupportedRole()) {
            cmbRole.addItem(role);
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblAccount.getModel();
        model.setRowCount(0);

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[2];
                row[0] = ua;
                row[1] = ua.getRole();
                ((DefaultTableModel) tblAccount.getModel()).addRow(row);
       
            }
        }   
    }
