    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String userName = txtUA.getText();
        String pwd1 = key1TX.getText();
        String pwd2=key2TX.getText();
        Organization organization = (Organization) cmbOrg.getSelectedItem();
        Person person = (Person) cmbPerson.getSelectedItem();
        Role role = (Role) cmbRole.getSelectedItem();
        if (person == null) {
            JOptionPane.showMessageDialog(null, "Select a person");
        } else if(business.checkIfUsernameIsUnique(userName)) {

            if(!isNameValid(userName)||!isPwdValid(pwd1)){ 
               }
         else if(pwd1.equals(pwd2)){
            if(role instanceof PetOwnerRole){
                organization.getUserAccountDirectory().createOwnerUserAccount(userName, pwd1, person, role);
//                enterprise.getUserAccountDirectory().createOwnerUserAccount(userName, pwd1, person, role);
//                business.getUserAccountDirectory().createOwnerUserAccount(userName, pwd2, person, role);
            }else{
                organization.getUserAccountDirectory().createUserAccount(userName, pwd1, person, role);
//                business.getUserAccountDirectory().createUserAccount(userName, pwd2, person, role);
//                enterprise.getUserAccountDirectory().createUserAccount(userName, pwd2, person, role);
            }
            txtUA.setText("");
            key1TX.setText("");
            key2TX.setText("");
          }else JOptionPane.showMessageDialog(null, "Passwords do not correspond!");
        } else {
            JOptionPane.showMessageDialog(null, "Username already exists");
        }

        populateTable();
    }//GEN-LAST:event_btnCreateActionPerformed


