    private void popTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblVital.getModel();
        dtm.setRowCount(0);
        Pet pet = request.getPet();
        for (VitalSign v : pet.getPetHealthInfo().getVitalSignList()) {
            Object row[] = new Object[4];
            row[0] = v.getPressure();
            row[1] = v.getPulse();
            row[2] = v.getGlucose();
            row[3] = Utilities.dateToString(v.getDate());
            dtm.addRow(row);
        }
    }

    private void fillText() {
        txtDescription.setText(request.getMessage());
    }

