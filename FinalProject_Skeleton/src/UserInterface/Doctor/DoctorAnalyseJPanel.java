    private void btnZoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZoomActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) tblVital.getModel();
        dtm.setRowCount(0);
        Pet pet = request.getPet();
        LocalDate sickDate = new LocalDate(request.getSickDate());
        //sickDate.minusDays(7);
        LocalDate today = new LocalDate();
        for (VitalSign v : pet.getPetHealthInfo().getVitalSignList()) {
            LocalDate date = new LocalDate(v.getDate());
            if (date.isAfter(sickDate.minusDays(7))) {
                Object row[] = new Object[4];
                row[0] = v.getPressure();
                row[1] = v.getPulse();
                row[2] = v.getGlucose();
                row[3] = v.getDate();
                dtm.addRow(row);
            }
        }
    }//GEN-LAST:event_btnZoomActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(!txtAdvice.getText().equals("")){
            request.setAdvice(txtAdvice.getText());
            request.setResolveDate(new Date());
            request.setStatus(Status.Finished.getValue());
            txtAdvice.setText("");
            btnSubmit.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Sent");
        }else{
            JOptionPane.showMessageDialog(null, "Respond can't be empty!");
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

