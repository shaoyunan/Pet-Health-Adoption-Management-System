    private void popCombo() {
        cmbOrg.removeAllItems();
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof HealthCareOrganization) {
                cmbOrg.addItem(org);
            }
        }
    }

    private void popTable(HealthCareOrganization hco) {
        DefaultTableModel dtm = (DefaultTableModel) tblList.getModel();
        dtm.setRowCount(0);

        for (AdoptionInfo a : hco.getAdoptionInfoList()) {
            Object row[] = new Object[4];
            row[0] = a;
            row[1] = a.getPet().getSpecies().getKind();
            row[2] = a.getPet().getOtherSpecies();
            row[3] = Utilities.dateToString(a.getDate());
            dtm.addRow(row);
        }
    }

