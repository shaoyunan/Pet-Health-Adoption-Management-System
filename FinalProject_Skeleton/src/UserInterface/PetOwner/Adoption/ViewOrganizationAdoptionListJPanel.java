    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Nothing Selected.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            HealthCareOrganization hco = (HealthCareOrganization) cmbOrg.getSelectedItem();
            AdoptionInfo adoptionInfo = (AdoptionInfo) tblList.getValueAt(selectedRow, 0);
            ViewAdoptionInfoJPanel viewAdoptionInfoJPanel = new ViewAdoptionInfoJPanel(userProcessContainer, business, enterprise, account, hco, adoptionInfo);
            userProcessContainer.add("viewAdoptionInfoJPanel", viewAdoptionInfoJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewActionPerformed


