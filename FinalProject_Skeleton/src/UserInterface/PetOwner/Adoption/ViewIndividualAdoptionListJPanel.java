    private void popTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblList.getModel();
        dtm.setRowCount(0);

        for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
            if (ua instanceof PetOwnerUserAccount&&!ua.getUsername().equals(account.getUsername())) {
                PetOwnerUserAccount poua = (PetOwnerUserAccount) ua;
                for (AdoptionInfo a : poua.getAdoptionInfoList()) {
                    Object row[] = new Object[5];
                    row[0] = a;
                    row[1] = a.getPet().getSpecies().getKind();
                    row[2] = a.getPet().getOtherSpecies();
                    row[3] = Utilities.dateToString(a.getDate());
                    row[4] = poua;
                    dtm.addRow(row);
                }
            }
        }
    }

