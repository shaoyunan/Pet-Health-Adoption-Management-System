    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Nothing Selected.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            AdoptionInfo adoptionInfo = (AdoptionInfo) tblList.getValueAt(selectedRow, 0);
            PetOwnerUserAccount targetAccount = (PetOwnerUserAccount)tblList.getValueAt(selectedRow, 4);
            ViewIAdoptionInfoJPanel viewIAdoptionInfoJPanel = new ViewIAdoptionInfoJPanel(userProcessContainer, business, account, adoptionInfo, targetAccount);
            userProcessContainer.add("viewIAdoptionInfoJPanel", viewIAdoptionInfoJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewActionPerformed


