    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        Pet pet = (Pet)cmbPet.getSelectedItem();
        if(txtDesc.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Enter some Info");
            return;
        }
        if(pet!=null){
            boolean check=true;
            for(AdoptionInfo aa:account.getAdoptionInfoList())
           if(aa.getPet().equals(pet))check=false; 
           if(check){ AdoptionInfo ai = new AdoptionInfo();
            ai.setPet(pet);
            ai.setInformation(txtDesc.getText());
            pet.setListed(true);
            account.getAdoptionInfoList().add(ai);
            cmbPet.removeAllItems();
            popTable();
            popCombo();
            txtDesc.setText("");
            JOptionPane.showMessageDialog(null, "Done!");}else JOptionPane.showMessageDialog(null, "You've already submited!");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

