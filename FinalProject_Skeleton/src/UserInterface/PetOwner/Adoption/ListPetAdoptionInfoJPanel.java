    private void popTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblRequest.getModel();
        dtm.setRowCount(0);

        for (AdoptionInfo ai : account.getAdoptionInfoList()) {
            Object row[] = new Object[3];
            row[0] = ai;
            row[1] = ai.getPet();
            row[2] = Utilities.dateToString(ai.getDate());
            dtm.addRow(row);
        }
    }

    private void popCombo(){
        cmbPet.removeAll();
        for(Pet pet : account.getPetDirectory().getPetList()){
            if(!pet.isListed()){
                cmbPet.addItem(pet);
            }
        }
    }
