    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRequest.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Nothing Selected.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            if ((JOptionPane.showConfirmDialog(null, "Do You Want to Delete This Record?", "Warning", JOptionPane.YES_NO_OPTION)) == JOptionPane.YES_OPTION) {
                AdoptionInfo a = (AdoptionInfo)tblRequest.getValueAt(selectedRow, 0);
                
                a.getPet().setListed(false);
                for(Pet p:account.getPetDirectory().getPetList())
                if(p.getName().equals(a.getPet().getName())&&p.getSpecies().getKind().equals(a.getPet().getSpecies().getKind())){
                p.setListed(false);
                break;
                    }
                for(AdoptionRequest ar : account.getAdoptionRequestQueue().getAdoptionRequestList()){
                    if(ar.getPet()==a.getPet()){
                        ar.setStatus(WorkRequest.Status.Declined.getValue());
                        ar.setRespond("The animal you required is no longer available.");
                    }
                }
                
                account.getAdoptionInfoList().remove(a);
                popTable();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


