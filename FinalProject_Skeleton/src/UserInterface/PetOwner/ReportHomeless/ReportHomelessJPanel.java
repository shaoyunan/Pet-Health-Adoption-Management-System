    public void popCombo(){
        cmbSpecies.removeAllItems();
        for(Animal.Species species : Animal.Species.values()){
            cmbSpecies.addItem(species);
        }
        
        cmbCondition.removeAllItems();
        for(HomelessAnimal.HealthCondition condition : HomelessAnimal.HealthCondition.values()){
            cmbCondition.addItem(condition);
        }
        
        cmbHC.removeAllItems();
        for(Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(org instanceof HealthCareOrganization){
                HealthCareOrganization o = (HealthCareOrganization)org;
                cmbHC.addItem(o);
            }
        }
    }
