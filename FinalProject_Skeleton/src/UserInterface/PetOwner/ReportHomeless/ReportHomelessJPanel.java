    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
        // TODO add your handling code here:
        HealthCondition condition = (HealthCondition)cmbCondition.getSelectedItem();
        if(!txtLocation.getText().equals("")){
            if (Utilities.stringToDate(txtDate.getText()) != null) {
                HomelessAnimal animal = new HomelessAnimal();
                animal.setSpecies((Species) cmbSpecies.getSelectedItem());
                animal.setHealthCondition(condition);

                HomelessReport report = new HomelessReport();
                if (!txtDate.getText().equals("")) {
                    report.setRequestDate(Utilities.stringToDate(txtDate.getText()));
                }
                report.setHomelessAnimal(animal);
                report.setLocation(txtLocation.getText());
                report.setSender(account);
                report.setMessage(txtDesc.getText());
                report.setRequestDate(Utilities.stringToDate(txtDate.getText()));
                report.setPriority(HomelessReport.Priority.Normal);
                if (!condition.getValue().equals(HealthCondition.Critical.getValue()) && cbFood.isSelected()) {
                    report.setPriority(HomelessReport.Priority.Low);
                }
                if (cbAggresive.isSelected() || condition.getValue().equals(HealthCondition.Critical.getValue()) || cbDanger.isSelected()) {
                    report.setPriority(HomelessReport.Priority.High);
                }

                HealthCareOrganization org = (HealthCareOrganization)cmbHC.getSelectedItem();
                org.getWorkQueue().getWorkRequestList().add(report);
                JOptionPane.showMessageDialog(null, "Submitted. Thank you for your report.");
                txtLocation.setText("");
                txtDesc.setText("");
                txtDate.setText(Utilities.dateToString(new Date()));
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please enter a location/address. Formatted address is prefered.");
        }
        
        
    }//GEN-LAST:event_btnReportActionPerformed


